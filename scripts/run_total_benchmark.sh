#!/bin/bash
# =============================================================================
# run_total_benchmark.sh — Uruchom benchmark totalny lokalnie
#
# Wymagania:
#   - git, python3, pip, gh (GitHub CLI zalogowany)
#   - maturin (do skanera Rust) LUB tylko Python fallback
#
# Użycie:
#   chmod +x run_total_benchmark.sh
#   ./run_total_benchmark.sh
#
# Wyniki trafią automatycznie na branch perplexity w repo qse-pkg.
# =============================================================================

set -e

REPO_URL="https://github.com/PiotrGry/qse-pkg.git"
BRANCH="perplexity"
REPOS_DIR="${HOME}/qse_total_bench"
QSE_DIR="${HOME}/qse-pkg"
ITER=3

echo "=================================================="
echo "QSE Benchmark Totalny — Iteracja ${ITER}"
echo "=================================================="
echo ""

# 1. Sklonuj / zaktualizuj qse-pkg
if [ ! -d "$QSE_DIR" ]; then
    echo "[1/6] Klonuję qse-pkg..."
    git clone --branch "$BRANCH" "$REPO_URL" "$QSE_DIR"
else
    echo "[1/6] Aktualizuję qse-pkg..."
    cd "$QSE_DIR"
    git fetch origin
    git checkout "$BRANCH"
    git pull origin "$BRANCH"
fi

cd "$QSE_DIR"

# 2. Zainstaluj zależności
echo ""
echo "[2/6] Instaluję zależności Python..."
pip install -e ".[dev]" -q 2>/dev/null || pip install -e . -q
pip install networkx scipy numpy -q

# Opcjonalnie: skaner Rust (szybszy, ale nie wymagany)
if command -v maturin &> /dev/null; then
    echo "  Buduję skaner Rust..."
    maturin develop -q 2>/dev/null || echo "  [warn] Rust build failed — Python fallback"
else
    echo "  [info] maturin nie znaleziony — używam Python scanera"
fi

# 3. Przygotuj katalog repo
echo ""
echo "[3/6] Przygotowuję katalog benchmarku: $REPOS_DIR"
mkdir -p "$REPOS_DIR"

# 4. Uruchom benchmark iteracyjny
echo ""
echo "[4/6] Uruchamiam experiment_total.py..."
echo "  Repos: scripts/repos_experiment_total.json"
echo "  Repos dir: $REPOS_DIR"
echo "  Output: artifacts/experiment_total/iter_${ITER}/"
echo ""

python3 scripts/experiment_total.py \
    --repos scripts/repos_experiment_total.json \
    --repos-dir "$REPOS_DIR" \
    --output-dir artifacts/experiment_total \
    --iter "$ITER"

# 5. Sprawdź czy wyniki są
RESULTS_JSON="artifacts/experiment_total/iter_${ITER}/results.json"
if [ ! -f "$RESULTS_JSON" ]; then
    echo ""
    echo "BŁĄD: Brak wyników w $RESULTS_JSON"
    exit 1
fi

REPOS_OK=$(python3 -c "import json; d=json.load(open('$RESULTS_JSON')); print(d.get('repos_ok', 0))")
echo ""
echo "Repos przeskanowanych: $REPOS_OK"

# 6. Wrzuć wyniki na GitHub
echo ""
echo "[5/6] Committuję wyniki..."
git add artifacts/experiment_total/
git commit -m "experiment/total: iter ${ITER} results — ${REPOS_OK} repos

Auto-generated by run_total_benchmark.sh
Branch: ${BRANCH}
Iter: ${ITER}
Repos OK: ${REPOS_OK}

Nowe metryki: GraphDensity, SCCEntropy, HubRatio, ProcessRisk" \
    2>/dev/null || echo "  (nic do commitowania)"

echo ""
echo "[6/6] Pushuje na branch $BRANCH..."
git push origin "$BRANCH"

echo ""
echo "=================================================="
echo "GOTOWE!"
echo "Wyniki: https://github.com/PiotrGry/qse-pkg/tree/${BRANCH}/artifacts/experiment_total/iter_${ITER}"
echo "=================================================="
